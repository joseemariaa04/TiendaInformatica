    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila=tablaPedidos.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, "No has seleccionado ningun pedido");
            return;
        }
        int id=(int)tablaPedidos.getValueAt(fila,0);
        for(Cliente c:FrmInicio.gestorTienda.getClientes()){
            for(Pedido p:c.getPedidos()){
                if(p.getId()==id){
                    switch(p.getEstadoPedido()){
                        case EstadoPedido.PENDIENTE:
                            p.setEstadoPedido(EstadoPedido.ENVIADO);
                            break;
                        case EstadoPedido.ENVIADO:
                            p.setEstadoPedido(EstadoPedido.ENTREGADO);
                            break;
                        default:
                            JOptionPane.showMessageDialog(rootPane, "Este pedido ya ha sido entregado");
                    }
                    break;
                }
            }
        }
        actualizarTabla();
    }//GEN-LAST:event_btnActualizarActionPerformed

