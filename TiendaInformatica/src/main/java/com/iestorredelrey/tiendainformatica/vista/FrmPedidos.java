    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        int fila = tablaPedidos.getSelectedRow();
        Pedido aBorrar=null;
        Cliente cliente=null;
        if(fila==-1){
            JOptionPane.showMessageDialog(rootPane, "No has seleccionado ningun pedido");
            return;
        }
        int id = (int)tablaPedidos.getValueAt(fila,0);
        for(Cliente c:FrmInicio.gestorTienda.getClientes()){
            for(Pedido p:c.getPedidos()){
                if(p.getId()==id){
                    aBorrar=p;
                    cliente=c;
                    break;
                }
            }
        }
        cliente.getPedidos().remove(aBorrar);
        actualizarTabla();
        actualizarTotal();
    }//GEN-LAST:event_btnBorrarActionPerformed

    public void actualizarTablaClientes(){
        JTableHeader header = tablaClientes.getTableHeader();
        header.setFont(new Font("Sans Serif", Font.BOLD, 18));//Poner fuente en la cabecera de la tabla
        tablaClientes.setAutoCreateRowSorter(true);
        ArrayList<Cliente> clientes=FrmInicio.gestorTienda.getClientes();
        if(clientes.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "No hay ningun cliente");
            return;
        }
        DefaultTableModel modelo=(DefaultTableModel)tablaClientes.getModel();
        modelo.setRowCount(0);
        for(Cliente c:clientes){
            modelo.addRow(new Object[]{c.getId(),c.getNombre()});
        }
    }
    public void actualizarTabla(){
        JTableHeader header = tablaPedidos.getTableHeader();
        header.setFont(new Font("Sans Serif", Font.BOLD, 18));//Poner fuente en la cabecera de la tabla
        tablaPedidos.setAutoCreateRowSorter(true);
        ArrayList<Pedido> pedidos = new ArrayList<>();
        for(Cliente c:FrmInicio.gestorTienda.getClientes()){
            for(Pedido p:c.getPedidos()){
                pedidos.add(p);
            }
        }
        DefaultTableModel modelotabla = (DefaultTableModel)tablaPedidos.getModel();
        modelotabla.setRowCount(0);
        total=0;
        for(Pedido p:pedidos){
            modelotabla.addRow(new Object[]{p.getId(),p.getCliente().getNombre(),p.getEstadoPedido(),p.getTotal()});
            total+=p.getTotal();//Incrementa el total de pedidos
        }
            actualizarTotal();
    }
    
        public void calcularUltimoId(){
        int maxID=0;
        for(Cliente c:FrmInicio.gestorTienda.getClientes()){
            for(Pedido p:c.getPedidos()){
                if(p.getId()>maxID){
                maxID=p.getId();
                }
            }
        }
        Pedido.setContador(maxID+1);
    }
        
    public void actualizarTotal(){
        NumberFormat nf = NumberFormat.getInstance();//Esto es para que salgan los puntos cada dos números
        nf.setMinimumFractionDigits(2);//Y esto para caparlo a dos decimales
        nf.setMaximumFractionDigits(2);

        lblTotal.setText("Total: " + nf.format(total)+"€");//por último lo asignamos al label de total
    }
    
